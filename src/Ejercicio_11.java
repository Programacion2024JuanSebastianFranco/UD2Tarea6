public class Ejercicio_11 {
    public static void main(String[] args) {
        for (int num = 1; num <= 8; num++) {
            System.out.print(num + " ");
        }

        System.out.println("\n");

        for (int num = 1; num <= 9; num+= 2) {
            System.out.print(num + " ");
        }

        System.out.println("\n");

        for (int num = 9; num >= 1; num-= 1) {
            System.out.print(num + " ");
        }

        System.out.println("\n");

        for (int num = 9; num >= 1; num-= 2) {
            System.out.print(num + " ");
        }

    }
}